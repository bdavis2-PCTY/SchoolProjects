				<br /><br /><p class="headerText">Bonobo Information</p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The bonobo, or formerly the pygmy chimpanzee, is an endangered great ape and one of the only to Pan species, or chimpanzees. The Bonobo originates in Africa in small groups found near the Congo river. Scientists believe that when the Congo river was created, some chimps were split off into the north and the south. Since they were split by the river and neither were good swimmers, several thousand years of the evolutionary process lead to the creation of bonobos.
	<br><br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;This species does relatively look like a common chimp, but with some key features that set it apart. They have longer than average legs that have a distinct pink skin underneath along with distinguished pink lips. The hair of a bonobo is much different than a chimp being parted and long on its head instead of being relatively short. Bonobos are generally a bit more slender than normal chimps and much smaller in overall mass. The bonobo also has a massive weight/size gap between genders. An adult male bonobo can range from 75 to 132 pounds whereas females have an average of only 66 pounds.
  <br><br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Bonobos were first said to be discovered in 1928. Ernst Schwarz, a German anatomist, studied a skull in the Tervuren museum that was thought to be the skull of a juvenile chimpanzee. After much research, Schwartz published his findings that this skull belonged to an entirely different species in 1929.


